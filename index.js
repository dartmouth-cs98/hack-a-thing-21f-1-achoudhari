/*
* This is the backend portion of the project that deals with server connections for the chat app
*/
const app = require("express")();
const server = require("http").createServer(app);
const cors = require("cors");

const io = require("socket.io")(server, {
	cors: {
		origin: "*",
		methods: [ "GET", "POST" ]
	}
});

app.use(cors());

const PORT = process.env.PORT || 5000;

app.get('/', (req, res) => {
	res.send('Running');
});

io.on("connection", (socket) => {
  // emit socket id upon connection
	socket.emit("me", socket.id);

  // broadcast a call ended message upon disconnection
	socket.on("disconnect", () => {
		socket.broadcast.emit("callEnded")
	});

  // emit necessary data upon call user event
	socket.on("callUser", ({ userToCall, signalData, from, name }) => {
		io.to(userToCall).emit("callUser", { signal: signalData, from, name });
	});

  // emit data signal upon answering call
	socket.on("answerCall", (data) => {
		io.to(data.to).emit("callAccepted", data.signal)
	});
});

server.listen(PORT, () => console.log(`Server is running on port ${PORT}`));